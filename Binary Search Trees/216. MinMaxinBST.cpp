class node {
public:
	int data;
	node* left;
	node* right;

	node(int d) {
		data = d;
		left = NULL;
		right = NULL;
	}
};

int minValue(Node* root)
{
	if (!root) return -1;

	while (root->left != NULL)
		root = root->left;

	return root->data;
}

